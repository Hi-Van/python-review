#printing a number
print(2)

#printing decimals
print(2.1189)

#printing negatives
print(-2)

#adding numbers
print(1 + 2)

#subtracting numbers
print(2 - 1)

#multiplying numbers
print(2 * 2)

#dividing numbers
print(2 / 2)

#remainders
print(10 % 3)

#python follows order of operations
print(2 * 3 + 4 - 1)
print(2 * (3 + 4 - 1))

#integer variables
my_int = 5

#float variables
